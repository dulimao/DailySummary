package com.example.myandroiddemo.card;

public class CardStyle {

    private String layout;
    private String pd;
    private int w;
    private int h;
    private String column;
    private int scale;
    private String mg;
    private int space_v;
    private int space_h;

    public String getLayout() {
        return layout;
    }

    public void setLayout(String layout) {
        this.layout = layout;
    }

    public String getPd() {
        return pd;
    }

    public void setPd(String pd) {
        this.pd = pd;
    }

    public int getW() {
        return w;
    }

    public void setW(int w) {
        this.w = w;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public String getColumn() {
        return column;
    }

    public void setColumn(String column) {
        this.column = column;
    }

    public int getScale() {
        return scale;
    }

    public void setScale(int scale) {
        this.scale = scale;
    }

    public String getMg() {
        return mg;
    }

    public void setMg(String mg) {
        this.mg = mg;
    }

    public int getSpace_v() {
        return space_v;
    }

    public void setSpace_v(int space_v) {
        this.space_v = space_v;
    }

    public int getSpace_h() {
        return space_h;
    }

    public void setSpace_h(int space_h) {
        this.space_h = space_h;
    }
}
